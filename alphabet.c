AC get_AC(int h, int w, A ASCII)
{
    int new_N = h * w; 
    int new_M = 1;
    AC ASCII_column;
    
    int a = 0, b= 0;
    ASCII_column.symbol = ASCII.symbol;
    for (int n = 0; n < new_N; n++)
        for (int m=0; m < new_M; m++)
        {
            ASCII_column.matrix[n][m] = ASCII.matrix[a][b];
            b++;
            if(b > new_M)
            {
                b = 0; a++;
            }
        }
    
    return ASCII_column;
}

