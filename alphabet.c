Alphabet_new get_alphabet_new(int h, int w, 
                                      Alphabet ASCII)
{
    int new_N = h * w; 
    int new_M = 1;
    Alphabet_new ASCII_column;
    
    int iter = 0;
    ASCII_column.symbol = ASCII.symbol;
    for (int n = 0; n < new_N; n++)
        for (int m=0; m < new_M; m++)
            ASCII_column.matrix[n][m] = *(&(ASCII.matrix[0][0]) + iter++);
    
    return ASCII_column;
}

